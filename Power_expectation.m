% Скрипт для вычисления среднего цвета изображения и создания монотонного изображения

% Шаг 1: Загрузка изображения
img = imread('10MHz.png'); % Замените 'image.png' на имя вашего файла

% Шаг 2: Удаление чёрных пикселей (где R, G, B = 0, 0, 0)
% Создаём маску для нечёрных пикселей
mask = ~(img(:,:,1) == 0 & img(:,:,2) == 0 & img(:,:,3) == 0);
% Применяем маску к изображению
img_no_black = img(repmat(mask, [1, 1, 3]));

% Шаг 3: Вычисление среднего цвета
mean_color = mean(img_no_black, 1); % Среднее по всем нечёрным пикселям

% Шаг 4: Округление средних значений для удобства
mean_color = round(mean_color);

% Шаг 5: Создание монотонного изображения 12x12
mono_img = uint8(ones(12, 12, 3) * mean_color);

% Шаг 6: Вывод изображения
figure; % Создаём новое окно
imshow(mono_img); % Отображаем монотонное изображение
title(['Средний цвет: RGB = [', num2str(mean_color(1)), ', ', num2str(mean_color(2)), ', ', num2str(mean_color(3)), ']']);

% Дополнительно: Вывод размерности для проверки
disp('Размер исходного изображения:');
disp(size(img));
disp('Размер монотонного изображения:');
disp(size(mono_img));
disp(['Средний цвет (RGB): ', num2str(mean_color)]);